// Lazy gallery: only resolve URLs when a specific miss's gallery is requested.
const galleryModules = import.meta.glob<{ default: string }>(
  '../../assets/miski_photo/**/*.{jpg,JPG,jpeg,png}',
  { eager: true },
);

const galleryCache = new Map<string, string[]>();

export function getGallery(folderName: string): string[] {
  const cached = galleryCache.get(folderName);
  if (cached) return cached;

  const result = Object.entries(galleryModules)
    .filter(([path]) => {
      const parts = path.split('/');
      return parts[parts.length - 2] === folderName;
    })
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([, mod]) => mod.default);

  galleryCache.set(folderName, result);
  return result;
}

